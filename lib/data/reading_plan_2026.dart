/// Builds the full list of [ReadingEntry] objects for the OTC 2026 plan.
/// Full-chapter sentinel (vs=0, ve=0) is resolved to real verse counts.
List<ReadingEntry> buildPlanEntries() {
  return _raw.map((r) {
    final (bookIdx, chapter, vs, ve, date) = r;
    final verseStart = vs == 0 ? 1 : vs;
    final verseEnd   = ve == 0
        ? bibleBooks[bookIdx].versesInChapter(chapter)
        : ve;
    return ReadingEntry(
      bookIndex:  bookIdx,
      chapter:    chapter,
      verseStart: verseStart,
      verseEnd:   verseEnd,
      readDate:   date,
    );
  }).toList();
}
